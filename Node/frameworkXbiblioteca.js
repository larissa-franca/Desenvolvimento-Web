//Os frameworks têm um caráter mais completo que as bibliotecas. 
//Biliotecas são mais pontuais, tendo um escopo de aplicação mais restrito. 
//É importante notar que o uso de bibliotecas e frameworks podem representar segurança e estabilidade em suas aplicações, 
//pois a tarefa de montar funcionalidades do zero, além de ser custosa, não garante que boas práticas de segurança e desempenho estejam 
//em uso. Um exemplo é o Express, um framework HTTP que garante a você estabilidade e tranquilidade futura quando utilizado. 
//Ao não usar um framework assim, você precisaria de bastante tempo e estudo para desenvolver as suas funcionalidades, 
//bem como para mantê-las atualizadas.